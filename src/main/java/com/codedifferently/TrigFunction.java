package com.codedifferently;

public class TrigFunction {
    public static double sine(double num){
        double result = Math.sin(num);
        return result;
    }

    public static double cosine(double num){
        double result = Math.cos(num);
        return result;
    }
    
    public static double tangent(double num){
        double result = Math.tan(num);
        return result;
    }

    


}